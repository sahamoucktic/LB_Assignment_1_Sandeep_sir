class Q15a
{
    public static void main(String args[])
    {
        //The number of bits used to represent a integer value
        System.out.println("The number of bits used to represent a integer value = "+Integer.SIZE);//32bits

        //The number of bytes used to represent a integer value
        System.out.println("The number of bytes used to represent a integer value = "+Integer.BYTES);//4BYTES

        //The minimum value a integer
        System.out.println("The minimum value a integer = "+Integer.MIN_VALUE);//-2147483648

        //The maximum value a integer
        System.out.println("The maximum value a integer = "+Integer.MAX_VALUE);//-2147483648

    }
}